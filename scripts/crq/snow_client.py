def _update_crq_index(source_path: str, snow_result: dict):
    """Update crq-index.json with the ServiceNow CHG number."""
    index_path = Path.home() / ".wibey" / "knowledge" / "crq-index.json"
    if not index_path.exists():
        return

    crq_number = snow_result.get("number", "")
    if not crq_number:
        return

    with open(index_path) as f:
        index = json.load(f)

    source_stem = Path(source_path).expanduser().stem
    for entry in index.get("crqs", []):
        crq_file = entry.get("crq_file", "")
        if source_stem in crq_file:
            entry["crq_number"] = crq_number
            entry["status"] = "SUBMITTED"
            entry["submitted_at"] = datetime.now(timezone.utc).isoformat()
            entry["snow_sys_id"] = snow_result.get("sys_id", "")
            break

    with open(index_path, "w") as f:
        json.dump(index, f, indent=2)

