def tool_get_crq(args: dict) -> dict:
    """
    GET a ServiceNow change request by CHG number.

    Args:
      number       (str, required) — e.g. "CHG3978198"
      show_all_fields (bool, default false) — if true, returns all raw fields
    """
    number = args.get("number", "").strip()
    if not number:
        return {"error": "number is required"}
    show_all = bool(args.get("show_all_fields", False))

    session = load_session()
    if not session:
        return session_expired_error()

    headers = _build_headers(session)
    query = f"number={number}"
    params = f"sysparm_query={query}&sysparm_limit=1"
    if not show_all:
        # Return a curated set of useful fields
        fields = ",".join([
            "number", "sys_id", "short_description", "state", "risk", "impact",
            "type", "category", "assignment_group", "cmdb_ci", "cab_required",
            "description", "u_implementation_plan", "u_validation_plan",
            "u_backout_plan", "start_date", "end_date", "opened_at",
            "u_affected_application", "u_change_classification",
        ])
        params += f"&sysparm_fields={fields}"

    path = f"{CHANGE_REQUEST_TABLE}?{params}"
    status, data, _ = _http("GET", path, headers)

    if status is None:
        return {"error": "connection_failed", "detail": str(data)}
    if status == 401:
        return session_expired_error()
    if status != 200:
        return {"error": f"http_{status}", "detail": str(data)[:500]}

    records = data.get("result", [])
    if not records:
        return {"error": "not_found", "number": number}

    record = records[0]
    # Flatten display_value from reference fields
    result = {}
    for k, v in record.items():
        if isinstance(v, dict):
            result[k] = v.get("display_value") or v.get("value") or ""
        else:
            result[k] = v

    return {"crq": result}


def tool_parse_crq_file(args: dict) -> dict:
    """
    Parse a .txt CRQ file (format from /crq-generator skill) into ServiceNow fields.
    Returns the field dict ready to pass to create_crq_draft.

    Args:
      path (str, required) — path to the .txt file (~ expansion supported)
    """
    path_str = args.get("path", "").strip()
    if not path_str:
        return {"error": "path is required"}

    path = Path(path_str).expanduser()
    if not path.exists():
        return {"error": "file_not_found", "path": str(path)}

    try:
        text = path.read_text()
    except Exception as e:
        return {"error": "read_failed", "detail": str(e)}

    lines = text.splitlines()
    fields: dict = {}
    current_section = None
    section_buffer: list[str] = []

    def flush_section():
        nonlocal section_buffer
        content = "\n".join(section_buffer).strip()
        if current_section == "summary":
            fields["short_description"] = content
        elif current_section == "description":
            fields["description"] = content
        elif current_section == "implementation":
            fields["u_implementation_plan"] = content
        elif current_section == "validation":
            fields["u_validation_plan"] = content
        elif current_section == "backout":
            fields["u_backout_plan"] = content
        section_buffer.clear()

    SECTION_MARKERS = {
        "Summary": "summary",
        "Description": "description",
        "Implementation plan": "implementation",
        "Validation plan": "validation",
        "Backout plan": "backout",
    }
    SEPARATOR_RE = re.compile(r"^[─\-]{5,}")

    meta: dict = {}
    for line in lines:
        stripped = line.strip()
        if stripped in SECTION_MARKERS:
            flush_section()
            current_section = SECTION_MARKERS[stripped]
            continue
        if SEPARATOR_RE.match(stripped):
            continue
        if current_section is None and ":" in line and not stripped.startswith("http"):
            key, _, val = line.partition(":")
            meta[key.strip().lower().replace(" ", "_")] = val.strip()
        elif current_section:
            section_buffer.append(line)

    flush_section()

    # Apply Walmart-standard fixed fields — read from team-config.yaml, fall back to built-ins
    snow = _snow_defaults()
    fields["state"] = STATE_DRAFT
    fields["assignment_group"] = snow["assignment_group"]
    fields["u_primary_escalation_group"] = snow["primary_escalation_group"]
    fields["u_secondary_escalation_group"] = snow["secondary_escalation_group"]
    fields["u_change_manager_group"] = snow["change_manager_group"]
    fields["type"] = "normal"
    fields["category"] = "application"
    fields["impact"] = "3"
    fields["u_market"] = snow["market"]
    fields["cmdb_ci"] = snow["cmdb_ci"]
    fields["u_affected_application"] = snow["affected_application"]
    fields["u_ci_class_name"] = "Business Application"
    fields["u_reason_for_install"] = meta.get("reason_for_install", "enhancement")
    fields["u_change_classification"] = "software"
    fields["u_testing_completed"] = "yes"
    fields["u_type_of_testing_completed"] = "technical_testing,functional_testing"
    fields["cab_required"] = "false"

    # Planned dates: next day 15:00 CDT → end day after at 15:00 CDT
    cdt_offset = timedelta(hours=-5)
    now_utc = datetime.now(timezone.utc)
    now_cdt = now_utc + cdt_offset
    next_day_cdt = (now_cdt + timedelta(days=1)).replace(hour=15, minute=0, second=0, microsecond=0)
    end_day_cdt = next_day_cdt + timedelta(days=1)
    fields["start_date"] = (next_day_cdt - cdt_offset).strftime("%Y-%m-%d %H:%M:%S")
    fields["end_date"] = (end_day_cdt - cdt_offset).strftime("%Y-%m-%d %H:%M:%S")

    geo_raw = meta.get("impacted_geos", "CA").strip()
    fields["u_affected_geos"] = geo_raw

    if not fields.get("short_description"):
        fields["short_description"] = path.stem

    return {"fields": fields, "source_file": str(path), "meta_parsed": meta}


def tool_create_crq_draft(args: dict) -> dict:
    """
    POST a new change request in Draft state (state=-5). Never auto-submits.

    Args:
      fields   (dict, required) — ServiceNow field dict (from parse_crq_file or manual)
      dry_run  (bool, default false) — if true, returns fields without POSTing
    """
    fields = args.get("fields")
    if not fields or not isinstance(fields, dict):
        return {"error": "fields dict is required"}

    dry_run = bool(args.get("dry_run", False))

    # Safety: always draft, never allow caller to escalate
    fields["state"] = STATE_DRAFT

    if not fields.get("short_description", "").strip():
        return {"error": "short_description is required in fields"}

    if dry_run:
        return {"dry_run": True, "would_post": fields}

    session = load_session()
    if not session:
        return session_expired_error()

    headers = _build_headers(session)
    status, data, _ = _http("POST", CHANGE_REQUEST_TABLE, headers, body=fields)

    if status is None:
        return {"error": "connection_failed", "detail": str(data)}
    if status == 401:
        return session_expired_error()
    if status not in (200, 201):
        return {"error": f"http_{status}", "detail": str(data)[:1000]}

    result = data.get("result", {})
    crq_number = result.get("number", "?")
    sys_id = result.get("sys_id", "?")
    crq_url = f"{SERVICENOW_BASE}/nav_to.do?uri=change_request.do?sys_id={sys_id}"

    return {
        "status": "created",
        "crq_number": crq_number,
        "sys_id": sys_id,
        "url": crq_url,
        "short_description": result.get("short_description", ""),
        "state": result.get("state", STATE_DRAFT),
        "note": "CRQ is in Draft. Review in ServiceNow before submitting.",
    }


def tool_search_incidents(args: dict) -> dict:
    """
    Search ServiceNow incidents by assignment group and/or state.

    Args:
      assignment_group (str, default "INTL-ET360") — assignment group name
      state            (str, default "active")      — "active", "resolved", "all"
      limit            (int, default 20)             — max records to return
      query            (str, optional)               — additional sysparm_query fragment
    """
    group = args.get("assignment_group", _snow_defaults()["assignment_group"]).strip()
    state_filter = args.get("state", "active").strip().lower()
    limit = int(args.get("limit", 20))
    extra_query = args.get("query", "").strip()

    session = load_session()
    if not session:
        return session_expired_error()

    # Build sysparm_query
    parts = [f"assignment_group.name={group}"]
    if state_filter == "active":
        parts.append("active=true")
    elif state_filter == "resolved":
        parts.append("state=6")  # 6 = Resolved in ServiceNow
    elif state_filter == "open":
        parts.append("state!=6^state!=7")  # exclude Resolved and Closed
    # "all" — no state filter
    if extra_query:
        parts.append(extra_query)

    sysparm_query = "^".join(parts)
    fields = ",".join([
        "number", "sys_id", "short_description", "state", "priority",
        "severity", "assigned_to", "assignment_group", "opened_at",
        "resolved_at", "category", "subcategory", "cmdb_ci",
        "description", "close_notes", "active",
    ])
    path = (
        f"{INCIDENT_TABLE}"
        f"?sysparm_query={sysparm_query}"
        f"&sysparm_limit={limit}"
        f"&sysparm_fields={fields}"
        f"&sysparm_display_value=true"
        f"&sysparm_order_by_direction=desc"
        f"&sysparm_order_by=opened_at"
    )

    headers = _build_headers(session)
    status, data, _ = _http("GET", path, headers)

    if status is None:
        return {"error": "connection_failed", "detail": str(data)}
    if status == 401:
        return session_expired_error()
    if status not in (200, 201):
        return {"error": f"http_{status}", "detail": str(data)[:500]}

    records = data.get("result", [])
    incidents = []
    for r in records:
        inc = {}
        for k, v in r.items():
            inc[k] = v.get("display_value") if isinstance(v, dict) else v
        incidents.append(inc)

    return {
        "assignment_group": group,
        "state_filter": state_filter,
        "count": len(incidents),
        "incidents": incidents,
    }


def tool_get_incident(args: dict) -> dict:
    """
    GET a ServiceNow incident by INC number.

    Args:
      number          (str, required) — e.g. "INC53036685"
      show_all_fields (bool, default false) — return all raw fields
    """
    number = args.get("number", "").strip()
    if not number:
        return {"error": "number is required"}
    show_all = bool(args.get("show_all_fields", False))

    session = load_session()
    if not session:
        return session_expired_error()

    headers = _build_headers(session)
    params = f"sysparm_query=number={number}&sysparm_limit=1&sysparm_display_value=true"
    if not show_all:
        fields = ",".join([
            "number", "sys_id", "short_description", "state", "priority",
            "severity", "assigned_to", "assignment_group", "opened_at",
            "resolved_at", "category", "subcategory", "cmdb_ci",
            "description", "close_notes", "active", "work_notes",
            "caller_id", "impact", "urgency",
        ])
        params += f"&sysparm_fields={fields}"

    path = f"{INCIDENT_TABLE}?{params}"
    status, data, _ = _http("GET", path, headers)

    if status is None:
        return {"error": "connection_failed", "detail": str(data)}
    if status == 401:
        return session_expired_error()
    if status != 200:
        return {"error": f"http_{status}", "detail": str(data)[:500]}

    records = data.get("result", [])
    if not records:
        return {"error": "not_found", "number": number}

    r = records[0]
    incident = {k: (v.get("display_value") if isinstance(v, dict) else v) for k, v in r.items()}
    return {"incident": incident}


def tool_get_my_ritm_approvals(args: dict) -> dict:
    """
    Get ServiceNow RITM (Requested Item) approvals pending for the current user.

    Queries sysapproval_approver where approver = current user AND state = requested,
    then enriches with RITM details (short_description, requested_for, cat_item).

    Args:
      include_all_types (bool, default false) — include non-RITM approvals too (CHG, CAP, etc.)
      limit             (int, default 50)     — max approval records to return
    """
    include_all = bool(args.get("include_all_types", False))
    limit = int(args.get("limit", 50))

    session = load_session()
    if not session:
        return session_expired_error()

    headers = _build_headers(session)

    # Step 1: get current user's sys_id
    status, data, _ = _http("GET", "/api/now/ui/user/current_user", headers)
    if status is None:
        return {"error": "connection_failed", "detail": str(data)}
    if status == 401:
        return session_expired_error()
    if status != 200:
        return {"error": f"http_{status}_on_current_user", "detail": str(data)[:300]}

    user_info = data.get("result", {})
    user_sys_id = user_info.get("user_sys_id", "")
    user_name = user_info.get("user_display_name", user_info.get("user_name", "unknown"))
    if not user_sys_id:
        return {"error": "could_not_resolve_user_sys_id", "raw": user_info}

    # Step 2: fetch pending approvals
    path = (
        "/api/now/table/sysapproval_approver"
        f"?sysparm_query=approver={user_sys_id}^state=requested"
        "&sysparm_display_value=true"
        f"&sysparm_limit={limit}"
        "&sysparm_fields=sys_id,state,approver,source_table,sysapproval,document_id,created_on,due_date,comments"
    )
    status, data, _ = _http("GET", path, headers)
    if status is None:
        return {"error": "connection_failed", "detail": str(data)}
    if status == 401:
        return session_expired_error()
    if status != 200:
        return {"error": f"http_{status}", "detail": str(data)[:500]}

    approvals_raw = data.get("result", [])

    def _flat(r):
        return {k: (v.get("display_value") if isinstance(v, dict) else v) for k, v in r.items()}

    all_approvals = [_flat(r) for r in approvals_raw]

    # Filter to RITM only unless include_all_types=True
    ritm_approvals = [a for a in all_approvals if a.get("source_table") == "sc_req_item"]
    other_approvals = [a for a in all_approvals if a.get("source_table") != "sc_req_item"]

    # Step 3: enrich RITM approvals with RITM details
    ritm_numbers = [a["sysapproval"] for a in ritm_approvals if a.get("sysapproval")]
    ritm_detail_map = {}
    if ritm_numbers:
        nums_query = "^ORnumber=".join(ritm_numbers)
        detail_path = (
            "/api/now/table/sc_req_item"
            f"?sysparm_query=number={nums_query}"
            "&sysparm_display_value=true"
            f"&sysparm_limit={len(ritm_numbers)}"
            "&sysparm_fields=number,short_description,state,requested_for,opened_by,cat_item,opened_at,request"
        )
        d_status, d_data, _ = _http("GET", detail_path, headers)
        if d_status == 200:
            for r in d_data.get("result", []):
                flat = _flat(r)
                ritm_detail_map[flat.get("number", "")] = flat

    # Merge
    enriched = []
    for a in ritm_approvals:
        ritm_num = a.get("sysapproval", "")
        detail = ritm_detail_map.get(ritm_num, {})
        enriched.append({
            "ritm_number":     ritm_num,
            "approval_state":  a.get("state"),
            "approval_due":    a.get("due_date"),
            "approval_sys_id": a.get("sys_id"),
            "cat_item":        detail.get("cat_item", ""),
            "short_description": detail.get("short_description", a.get("document_id", "")),
            "ritm_state":      detail.get("state", ""),
            "requested_for":   detail.get("requested_for", ""),
            "opened_by":       detail.get("opened_by", ""),
            "opened_at":       detail.get("opened_at", ""),
            "parent_request":  detail.get("request", ""),
        })

    result = {
        "user": user_name,
        "user_sys_id": user_sys_id,
        "ritm_approvals_count": len(enriched),
        "ritm_approvals": enriched,
    }

    if include_all:
        result["other_approvals_count"] = len(other_approvals)
        result["other_approvals"] = other_approvals

    return result


