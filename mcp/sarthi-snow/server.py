# ── Session loader ────────────────────────────────────────────────────────────
def load_session() -> dict | None:
    if not SESSION_FILE.exists():
        return None
    try:
        with open(SESSION_FILE) as f:
            return json.load(f)
    except Exception as e:
        log(f"WARN: Cannot read {SESSION_FILE}: {e}")
        return None


def session_expired_error() -> dict:
    return {
        "error": "session_expired",
        "message": "ServiceNow session cookie expired or missing.",
        "fix": "ServiceNow session expired. Use sarthi-snow-auth MCP: call refresh_session tool, or run: python3 ~/sarthi/scripts/crq/extract_snow_session.py",
    }


# ── HTTP helpers ──────────────────────────────────────────────────────────────
def _http(method: str, path_with_query: str, headers: dict, body: dict | None = None) -> tuple[int, dict | str]:
    """Low-level HTTPS request via system proxy. Returns (status, parsed_json_or_raw_str)."""
    try:
        url = f"{SERVICENOW_BASE}{path_with_query}"
        body_bytes = json.dumps(body).encode() if body is not None else None
        req = urllib.request.Request(url, data=body_bytes, headers=headers, method=method)
        # urllib.request honours https_proxy / HTTPS_PROXY env vars automatically
        proxy_handler = urllib.request.ProxyHandler()
        opener = urllib.request.build_opener(proxy_handler)
        with opener.open(req, timeout=TIMEOUT) as resp:
            raw = resp.read().decode("utf-8", errors="replace")
            token = resp.headers.get("X-UserToken-Response", "")
            try:
                return resp.status, json.loads(raw), token
            except json.JSONDecodeError:
                return resp.status, {"_raw": raw}, token
    except urllib.error.HTTPError as e:
        raw = e.read().decode("utf-8", errors="replace")
        token = e.headers.get("X-UserToken-Response", "")
        try:
            return e.code, json.loads(raw), token
        except json.JSONDecodeError:
            return e.code, {"_raw": raw}, token
    except Exception as e:
        return None, str(e), ""


def _get_user_token(session: dict) -> str:
    """Obtain X-UserToken by making a lightweight GET. Caches in session dict."""
    if session.get("x_user_token"):
        return session["x_user_token"]
    headers = {
        "Cookie": session.get("cookie_header", ""),
        "Accept": "application/json",
    }
    path = f"{CHANGE_REQUEST_TABLE}?sysparm_limit=1&sysparm_fields=number"
    status, _, token = _http("GET", path, headers)
    if token:
        session["x_user_token"] = token
    return token


def _build_headers(session: dict) -> dict:
    token = _get_user_token(session)
    return {
        "Cookie": session.get("cookie_header", ""),
        "Content-Type": "application/json",
        "Accept": "application/json",
        "X-UserToken": token,
    }


